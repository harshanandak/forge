#!/usr/bin/env node
// Forge Dashboard — snapshot generator (read-only).
//
// Shells the Forge CLI (`forge issue list/status --json`) and bakes the result
// into `snapshot.js` (a browser global, so index.html opens by double-click with
// NO server) and `data.json` (machine-readable, same payload).
//
// Usage:  node web/dashboard/generate-snapshot.mjs
// Run from anywhere inside the repo/worktree; it locates bin/forge.js by walking up.

import { execFileSync } from 'node:child_process';
import { existsSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));

function findRepoRoot(start) {
  let dir = start;
  for (let i = 0; i < 8; i++) {
    if (existsSync(join(dir, 'bin', 'forge.js'))) return dir;
    const up = resolve(dir, '..');
    if (up === dir) break;
    dir = up;
  }
  throw new Error('Could not locate bin/forge.js above ' + start);
}

const ROOT = findRepoRoot(HERE);
const FORGE = join(ROOT, 'bin', 'forge.js');

function forge(args) {
  const out = execFileSync(process.execPath, [FORGE, ...args], {
    cwd: ROOT,
    encoding: 'utf8',
    maxBuffer: 64 * 1024 * 1024,
  });
  return JSON.parse(out);
}

function safe(args, fallback) {
  try {
    return forge(args);
  } catch (err) {
    console.warn(`  ! forge ${args.join(' ')} failed: ${err.message.split('\n')[0]}`);
    return fallback;
  }
}

console.log('Forge dashboard snapshot');
console.log('  repo:', ROOT);

const listRes = forge(['issue', 'list', '--json']);
const issues = listRes?.data?.issues ?? [];
console.log(`  issues: ${issues.length}`);

const statusRes = safe(['status', '--json'], null);
const recallRes = safe(['recall', '--json'], []);
const recall = Array.isArray(recallRes) ? recallRes : recallRes?.data ?? [];

const snapshot = {
  generated_at: new Date().toISOString(),
  schema_version: listRes?.schema_version ?? null,
  source: 'forge issue list --json + forge status --json + forge recall --json',
  counts: { issues: issues.length },
  status: statusRes,
  memory: recall,
  issues,
};

const json = JSON.stringify(snapshot);
writeFileSync(join(HERE, 'data.json'), JSON.stringify(snapshot, null, 2));
writeFileSync(
  join(HERE, 'snapshot.js'),
  `/* AUTO-GENERATED by generate-snapshot.mjs — do not edit by hand. */\n` +
    `window.FORGE_SNAPSHOT = ${json};\n`,
);

console.log('  wrote: web/dashboard/snapshot.js');
console.log('  wrote: web/dashboard/data.json');
console.log(`  done @ ${snapshot.generated_at}`);
