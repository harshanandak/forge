// Generate lib/embedded-assets.generated.mjs — a Bun-embeddable manifest that
// statically imports every runtime asset (with { type: "file" }) and maps its
// real relative POSIX path to the embedded-file path Bun resolves at compile time.
//
// WHY a generated per-file map instead of `bun build --compile ... skills/**`:
// Bun's `embeddedFiles` exposes only a HASHED, directory-less `blob.name`
// (e.g. `SKILL-a1b2c3.md`), so every skill's `SKILL.md` would collide and the
// tree could not be reconstructed. A generated map keyed by the true relative
// path preserves the directory structure exactly; each value is that specific
// file's `/$bunfs/…` path, so `Bun.file(value)` / `fs` reads the right bytes.
//
// The output is gitignored and regenerated by the `build:binary` script right
// before `bun build --compile`, so it can never drift from the source tree. The
// completeness test (test/embedded-assets-drift.test.js) walks the same roots and
// asserts the embed set == the on-disk asset set, both ways.

import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';
import { fileURLToPath } from 'node:url';
import pkgRoot from '../lib/package-root.js';

const { ASSET_ROOTS } = pkgRoot;

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(REPO_ROOT, 'lib', 'embedded-assets.generated.mjs');

// Deterministic, locale-INDEPENDENT codepoint comparator. Locale-default sort would
// diverge by machine locale and produce different embed bytes across the step-3
// cross-compile targets, so sorting is pinned to codepoint order everywhere.
const byteCompare = (a, b) => (a < b ? -1 : a > b ? 1 : 0);

/** True when the asset needs the executable bit restored on extraction (files-only embedding drops mode). */
export function isExecutableAsset(relPosixPath) {
  return relPosixPath.endsWith('.sh') || relPosixPath.startsWith('.forge/hooks/');
}

/** Recursively list files under an absolute path, returning repo-relative posix paths. Symlinks are skipped (materialize content, never embed links). */
function walk(absPath, relPath, out) {
  const stat = fs.lstatSync(absPath);
  if (stat.isSymbolicLink()) return;
  if (stat.isDirectory()) {
    for (const entry of fs.readdirSync(absPath).sort(byteCompare)) {
      walk(path.join(absPath, entry), `${relPath}/${entry}`, out);
    }
  } else if (stat.isFile()) {
    out.push(relPath);
  }
}

/** Collect every embeddable asset as a sorted list of repo-relative posix paths. */
export function collectAssetFiles(repoRoot = REPO_ROOT, roots = ASSET_ROOTS) {
  const files = [];
  for (const root of roots) {
    const abs = path.join(repoRoot, root);
    if (!fs.existsSync(abs)) {
      // Fail CLOSED: a configured asset root that is absent would silently ship an
      // incomplete manifest. Error instead so the build stops loudly.
      throw new Error(
        `Configured ASSET_ROOT does not exist: ${root} (looked in ${abs}). ` +
          'Fix ASSET_ROOTS in lib/package-root.js or restore the missing path.'
      );
    }
    walk(abs, root.split(path.sep).join('/'), files);
  }
  return [...new Set(files)].sort(byteCompare);
}

/**
 * Content fingerprint over the asset SET: sha256 of each relpath + its bytes, in
 * sorted order. Emitted into the manifest so the runtime cache dir identity
 * changes whenever any asset content or the set changes (invalidates stale
 * same-version extractions). Deterministic across machines.
 */
export function assetFingerprint(repoRoot = REPO_ROOT, files = collectAssetFiles(repoRoot)) {
  const h = crypto.createHash('sha256');
  for (const rel of files) {
    h.update(rel);
    h.update('\0');
    h.update(fs.readFileSync(path.join(repoRoot, ...rel.split('/'))));
  }
  return h.digest('hex');
}

function render(files, fingerprint) {
  const imports = [];
  const entries = [];
  const execEntries = [];
  files.forEach((rel, i) => {
    const ident = `a${i}`;
    // Specifier is relative to lib/embedded-assets.generated.mjs → prefix `../`.
    const specifier = JSON.stringify(`../${rel}`);
    imports.push(`import ${ident} from ${specifier} with { type: "file" };`);
    entries.push(`  ${JSON.stringify(rel)}: ${ident},`);
    if (isExecutableAsset(rel)) execEntries.push(`  ${JSON.stringify(rel)},`);
  });
  return [
    '// AUTO-GENERATED by scripts/gen-embedded-assets.mjs — DO NOT EDIT.',
    '// Gitignored; regenerated before every `bun build --compile`.',
    ...imports,
    '',
    'export const EMBEDDED_ASSETS = {',
    ...entries,
    '};',
    '',
    'export const EXECUTABLE_ASSETS = [',
    ...execEntries,
    '];',
    '',
    `export const ASSET_FINGERPRINT = ${JSON.stringify(fingerprint)};`,
    '',
  ].join('\n');
}

function main() {
  const files = collectAssetFiles();
  const fingerprint = assetFingerprint(REPO_ROOT, files);
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, render(files, fingerprint), 'utf8');
  const execCount = files.filter(isExecutableAsset).length;
  console.log(
    `Embedded ${files.length} asset file(s) (${execCount} executable), fingerprint ${fingerprint.slice(0, 16)} → ${path.relative(REPO_ROOT, OUT_FILE)}`
  );
}

if (import.meta.main || process.argv[1] === fileURLToPath(import.meta.url)) {
  main();
}
